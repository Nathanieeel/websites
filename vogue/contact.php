    <h3>7. Procedure for Reporting Security Incident</h3>
    <p>
      Security incidents, events, and weaknesses in the data processing system need to be reported at the earliest possible stage 
      to enable assessment by the Data Protection Officer (DPO) and the Head of the Security Incident Response Team (SIRT). The DPO 
      supports the SIRT in identifying when a series of events or weaknesses have escalated to an incident. The SIRT needs detailed information from data subjects or personnel to determine whether an incident is occurring.
    </p>

    <p>Examples of Security Events, weaknesses, and common forms of Security Incidents have been provided below:</p>
    <p>Security events can include</p>
    <ul>
      <li>  ●Uncontrolled system changes</li>
      <li>  ●Access violations – e.g., password sharing</li>
      <li>  ●Breaches of physical security:
        <ul>
          <li>  ●Loss of ID badges</li>
          <li>  ●Missing correspondence</li>
          <li>  ●Exposure of uncollected print-outs</li>
        </ul>
      </li>
      <li>  ●Misplaced or missing media</li>
      <li>  ●Loss of mobile phones and portable devices</li>
      <li>  ●Systems being hacked or manipulated</li>
      <li>  ●Non-compliance with policies</li>
    </ul>

    <p>Security weaknesses can include:</p>
    <ul>
      <li>  ●Inadequate firewall or antivirus protection</li>
      <li>  ●System malfunctions or overloads</li>
      <li>  ●Malfunctions of software applications</li>
      <li>  ●Human errors</li>
    </ul>

    <p>The most common Security Incidents are listed below. It should be noted that this list is not exhaustive.</p>
    <p>Malicious</p>
    <ul>
      <li>  ●Giving information to unauthorized individuals (verbally, in writing, or electronically)</li>
      <li>  ●Computer infected with malware</li>
      <li>  ●Sending a sensitive e-mail to 'all staff' by mistake</li>
      <li>  ●Receiving unsolicited mail of an offensive nature.</li>
      <li>  ●eceiving unsolicited mail that requires you to enter personal data.</li>
      <li>  ●Finding data that has been changed by an unauthorized person.</li>
      <li>  ●Receiving and forwarding chain letters – including virus warnings, scam warnings, and other emails that encourage the recipient to forward to others.</li>
      <li>  ●Unknown people ask for information that could gain them access to the University data (e.g. a password or details of a third party).</li>
    </ul>
    <p>Misuse</p>
    <ul>
      <li>  ●Use of unapproved or unlicensed software on the University’s equipment.</li>
      <li>  ●Accessing a computer database using someone else's authorization (e.g. someone else's user ID and password).</li>
      <li>  ●Writing down your password and leaving it on display / somewhere easy to find.</li>
      <li>  ●Printing or copying confidential information and not storing it correctly or confidentially.</li>
    </ul>
    <p>Theft or Loss</p>
    <ul>
      <li>  ●Theft or loss of hard copy files</li>
      <li>  ●Theft or loss of computer equipment</li>
    </ul>

    <p>7.1. Notification or Report to the Data Protection Officer and the Security Incident Response Team (SIRT)
    Security Incident Report shall be submitted or coordinated through:</p>
    <p>  The Data Protection Officer (DPO) and/or</p>
    <p>  Head of the Security Incident Response Team (SIRT)</p>
    <p>  Email addresses: dpo-notification@neu.edu.ph</p>
    <p>  Contact Numbers:028-9814221 local 3859</p>

    <p>The following information should be provided:</p>
    <ul>
      <li>  ●Details of the incident</li>
      <li>  ●Date and time of incident</li>
      <li>  ●Number of persons affected</li>
      <li>  ●Name of the office processing the information</li>
      <li>  ●Contact name and number of the person reporting the incident.</li>
      <li>  ●The type of data, information, or equipment involved.</li>
      <li>  ●Whether the loss of the data puts any person or other data at risk.</li>
      <li>  ●Location of the incident</li>
      <li>  ●Inventory numbers of any equipment affected.</li>
      <li>  ●Location of data or equipment affected.</li>
    </ul>

    <p>7.2. The SIRT shall investigate and assess the reported suspected security incidents in coordination with the concerned unit or office.</p>
    <p>7.3. The SIRT shall inform immediately, or within one (1) hour from the discovery of the security incident, to the University President for appropriate action. A Security Incident Report shall be accomplished within twenty-four (24) hours and submit the same to the University President.</p>
    <p>7.4. The SIRT shall evaluate the Security Incident Report submitted to them. The Head of the SIRT will determine if additional members or experts are necessary to investigate the reported incident. If deemed necessary, the Head of the SIRT shall recommend the designation of additional members to the University President.</p>
    <p>7.5. The SIRT shall conduct its investigation based on the Security Incident Report submitted and shall perform the following tasks:</p>
    <ul>
      <li>  a. Collate the following information:
        <ul>
          <li>    i. The nature and circumstances of the security incident.</li>
          <li>    ii. Data processing systems involved.</li>
          <li>    iii. Data Processor and other persons responsible or involved, designation and their contact details.</li>
        </ul>
      </li>
      <li>  b. Conduct clarificatory questions to the Data Processor.</li>
      <li>  c. Require additional submissions of documents, reports, or information from the Data Processor.</li>
      <li>  d. Request for a meeting with the Data Processor and other concerned offices or individuals in the University.</li>
      <li>  e. Perform actions to obtain the necessary information related to the reported Security Incident.</li>
      <li>  f. Consult with the DPO and Head of the SIRT before performing major actions that may need permission from the University President and/or Board of Trustees.</li>
      <li>  g. Complete its investigation within forty-eight (48) hours after the endorsement of the Security incident Report for investigation. Any request for extension of investigation and submission of initial assessment shall be relayed to the University President, DPO, and Head of the SIRT.a</li>
    </ul>
    
    <p>7.6. The results of the investigation and initial recommendation (SIRT Security Incident Report) shall be signed by all members of the SIRT and submitted to the University President, copy furnished to the DPO.</p>
    <p>7.7. The University President, DPO and Head of the SIRT shall then make a final recommendation to the Board of Trustees for appropriate approval and action.</p>
    <p>7.8. All documents submitted related to the Security Incident shall be consolidated and kept by the DPO and Head of the SIRT.</p>

    <h3>8. Security Measures</h3> 
    <p>8.1. The DPO and Head of the SIRT, with the approval of the University President, will conduct a debriefing session with the COPs, Data Processor and concern individuals to implement remedial or preventive measures. The DPO may also conduct an orientation regarding data privacy and compliance with the Data Privacy Act of 2012.</p>
    <p>8.2. The DPO and Head of the SIRT may recommend the conduct of Privacy Impact Assessment (PIA) on the data processing system involved in the security incident or the office concern.</p>
    <p>8.3. The University President shall issue a corresponding memorandum to implement the new security measures as recommended by the DPO and the Head of the SIRT.</p>
    <p>8.4. The SIRT shall conduct the following:</p>
    <ul>
        <li>  a. Regular monitoring for security breaches and vulnerability scanning of computer networks;</li>
        <li>  b. Change user and administrative access credentials;</li>
        <li>  c. Conduct backup of data as deemed necessary. General or scheduled back-up shall be implemented by CSD;</li>
        <li>  d. Shut down or disconnect the affected devices from the internet or intranet;</li>
        <li>  e. Conduct inventory and review of access control, equipment and assets, and secure log files as deemed necessary. General inventory shall be done by CSD and reported to the SIRT and DPO;</li>
        <li>  f. Restore, repair or rebuild the system or application that was compromised.</li>
        <li>  g. Review and approve the Assets Inventory Report conducted by the CSD.</li>
    </ul>

    <p>8.5. The Computer Service Department (CSD) shall remain to be responsible for the daily operation and maintenance of the University’s I.T. facilities. They shall immediately report to the SIRT in case there is a belief or possibility that a security incident might arise and need further investigation by the SIRT. They will cooperate and allow the SIRT and DPO to regularly check the operations of the CSD to comply with the former’s obligation to monitor security breaches and vulnerability of computer networks.</p>
    <p>8.6. The Acceptable Use Policy of the University, including but not limited to the Information Security Policy, shall govern the use of computing facilities and network infrastructure in the University.</p>


    <h3>9. Confidentiality</h3> 
    <p>All information related to reported security incidents shall be kept confidential by all concerned Parties. A corresponding Non-Disclosure Agreement prescribed by the DPO shall be explained to the concerned parties and signed by them before they assume their functions under this Policy. Any public pronouncements involving any security incidents must be upon the approval of the DPO, University President, and/or Board of Trustees.</p>
    
    <h3>10. Data Breach Notification of the NPC and Data Subjects</h3> 
    <p>The DPO shall assess and recommend whether the Personal Data Breach should be reported to the National Privacy Commission (NPC) as required under the Data Privacy Act of 2012.</p>
    <p>The following shall be reported to NPC:</p>

    <ul>
    <li>  a. The personal data involves sensitive personal information or any other information that may be used to enable identity fraud;</li>
      <li>  b. Other information includes, but is not limited to, the following:
        <ul>
          <li>    i. Data about the financial or economic situation of the data subject;</li>
          <li>    ii. Usernames, passwords, and other login data;</li>
          <li>    iii. Biometric data;</li>
          <li>    iv. Copies of identification documents, licenses, or unique identifiers like Philhealth, SSS, GSIS, TIN number; or</li>
          <li>    v. Other similar information, which may be made the basis of decisions concerning the data subject, including the grant of rights or benefits.</li>
        </ul>
      </li>
      <li>  c. There is reason to believe that the information may have been acquired by an unauthorized person; and</li>
      <li>  d. The personal information controller believes that the data breach is likely to give rise to a real risk of serious harm to the affected data subject..</li>
    </ul>

    <p>When there is doubt as to whether notification is necessary, consider factors:</p>
    <ul>
    <li>  a. The likelihood of harm or negative consequences on the affected data subjects;</li>
    <li>  b. How notification, particularly of the data subjects, could reduce the risks arising from the personal data breach reasonably believed to have occurred; and</li>
    <li>  c. If the data involves:
        <ul>
            <li>    i. Information that would likely affect national security, public safety, public order, or public health;/li>
          <li>    ii. At least one hundred (100) individuals;</li>
          <li>    iii. Information required by all applicable laws or rules to be confidential; or</li>
          <li>    iv. Personal data of vulnerable groups.</li>
        </ul>
    </li>
    </ul>
    <p>Aside from notifying the NPC, the University, through the DPO, SIRT, and University President, shall also notify the affected Data Subjects within seventy-two (72) hours upon knowledge of or the reasonable belief by the University that a Personal Data Breach has occurred.
    Generally, there shall be no delay in notification, except to the extent necessary to determine the following:</p>
    <ul>
    <li>  a. The scope of the breach;</li>
    <li>  b. To prevent further disclosures; or</li>
    <li>  c. To restore reasonable integrity to the information and communications system.</li>
    </ul>

    <h3>11. Learning from Information Security Incidents</h3> 
    <p>Security Incidents shall be recorded and reviewed to improve the response process of the University. The information collated shall be considered in reviewing and recommending the revision of the University’s Data Privacy Policy.</p>

