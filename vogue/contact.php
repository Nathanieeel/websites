      <h2>Security Incident Management Policy</h2>
      <p>This policy provides guidance, procedures, and actions to be taken in case of security or personal breach involving any data processing system of the University and/or personal data under its control and custody..</p>

      <h3>1. Objective</h3> 

      <p>This policy is promulgated to:</p>
        <ul>
            <li>  ● Ensure that the University, its offices, personnel, and students react appropriately to any actual or suspected security incidents relating to the data processing systems and personal data within the custody of the University.</li>
            <li>  ● Establish a data breach response team and define its roles and responsibilities.</li>
            <li>  ● Implement security measures to prevent or minimize the occurrence of a personal data breach and assure the timely discovery of a security incident.</li>
            <li>  ● Establish an incident response procedure to ensure the availability, integrity, and confidentiality of the personal data being processed through the University’s data processing system.</li>
            <li>  ● Establish procedures for mitigating possible harm and negative consequences in the event of data breach.</li>        
        </ul>
      
        <h3>2. Scope</h3> 
        <p>This policy governs all New Era University’s branches, academic units and administrative offices, personnel, and students.</p>

        <h3>3. Definition of Terms</h3> 

        <p>For the purpose of this policy, the following terms are defined, as follows:</p>
        <p>3.1. “Personal Data” refers to personal information, sensitive personal information, and privileged information as defined by the Data Privacy Act of 2012;</p>
        <p>3.2. “Sensitive Personal Information” refers to personal information:</p>
        <p>
          a. About an individual’s race, ethnic origin, marital status, age, color, and religious, philosophical or political affiliations;</p>
        <p>  b. About an individual’s health, education, genetic or sexual life of a person, or to any proceeding for any offense committed or alleged to have been committed by such person, the disposal of such proceedings, or the sentence of any court in such proceedings;</p>
        <p>  c. Issued by government agencies peculiar to an individual which includes, but not limited to, social security numbers, previous or current health records, licenses or its denials, suspension or revocation, and tax returns, and</p>
        <p>  d. Specifically established by an executive order or an act of Congress to be kept classified.</p>
        <p>
        3.3. “Data Processing System” refers to the structure and procedure by which personal data is collected and processed in an information and communications system, or any other relevant filing system. It includes the purpose and intended output of the processing (National Privacy Commission, op.cit.).</p>
        <p>3.4. “Personal Data Breach” refers to a breach of security leading to the accidental or unlawful destruction, loss, alteration, unauthorized disclosure of, or access to, personal data transmitted, stored, or otherwise processed. A personal data breach may be in the nature of:</p>
        <p>
          a. An availability breach resulting from loss, accidental or unlawful destruction of personal data;</p>
        <p>  b. Integrity breach resulting from alteration of personal data; and/or</p>
        <p>  c. A confidentiality breach resulting from the unauthorized disclosure of or access to personal data.
        </p>
        <p>3.5. “Data Processor” refers to the NEU personnel who process Personal Data pertaining to a Data Subject;</p>
        <p>3.6. “University” refers to New Era University and all its branches.</p>
        <p>3.7. “Data Subject” refers to an individual whose personal data is processed.</p>
        <p>3.8. “Personnel” refers to all members of the faculty, also called as the academic personnel, both to the teaching and non-teaching personnel, working in the New Era University Main Campus in New Era, Quezon City and in its branches in Lipa City, Batangas; City of San Fernando, Pampanga; General Santos City, South Cotabato; and Pinugay, Baras, Rizal.
        </p>
        <p>3.9. “Security Incident Response Team (SIRT)” refers to the personnel that would be the first responders to any and all data breach incidents. The team is also tasked to provide regular updates to the affected University personnel as to the ongoing measures to address the security incident.</p>
        <p>3.10. “Compliance Officer for Privacy (COP)” refers to the personnel assigned to each department of the University to oversee compliance of data privacy within the department.
        </p>
        <p>3.11. “Security Incident” refers to an event or occurrence that affects or tends to affect data protection, or may compromise the availability, integrity, and confidentiality of personal data. It shall include incidents that would result to a personal data breach, if not for safeguards that have been put in place. The term “Security Incident” includes “Personal Data Breach.”</p>
        <p>A Security Incident includes, but is not restricted to, the following:</p>
        <ul>
            <li>  ● The loss or theft of data or information.</li>
            <li>  ● The transfer of data or information to those who are not entitled to receive that information.</li>
            <li>  ● Attempts (either failed or successful) to gain unauthorised access to data or information storage or a computer system..</li>
            <li>  ● Unwanted disruption or denial of service to a system.</li>
            <li>  ● The unauthorised use of a system for the processing or storage of data by any person.</li>
        </ul>

        <p>3.12. “Incident Report” refers to a document that provides a detailed account of a suspected security incident which could be a basis for initial assessment of the incident.
        </p>
        <p>3.13. “Risk” refers to the potential of an incident to result in harm or danger to a Data Subject or organization.</p>
        <p>3.14. “Vulnerability” refers to a weakness of a data processing system that makes it susceptible to threats and other attacks.</p>
        <p>3.15. “Threat” refers to a potential cause of an unwanted incident, which may result in harm or danger to a Data Subject, system, or organization.</p>
        <p>3.16. “DPA refers to Republic Act No. 10173, otherwise known as the Data Privacy Act of 2012.</p>
        <p>3.17. “IRR refers to the Implementing Rules and Regulations of Republic Act No. 10173, otherwise known as the Data Privacy Act of 2012.</p>
        <p>3.18. “NPC” refers to the National Privacy Commission of the Philippines as created by the Data Privacy Act of 2012.</p>


        <h3>4. Security Incident Response Team (SIRT) and Compliance Officer for Privacy (COP)</h3> 
        <p>The NEU Security Incident Response Team (SIRT) shall be responsible for investigation of a suspected security incident and reporting to the University President and Board of Trustees.</p>
        <p>The COP of each department shall serve as the main point of contact for all reports of a suspected security incident coming from their assigned department.</p>

        <h3>5. Composition of Security Incident Response Team (SIRT)</h3> 
