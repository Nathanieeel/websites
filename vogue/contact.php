        <h3>6. Duties and Responsibilities</h3> 

        <p>6.1. CSD</p>
    <ul>
      <li>  a. Responsible for the daily operation and maintenance of the University’s I.T. facilities.</li>
      <li>  b. Responsible for immediately reporting possible security incidents that need further investigation by the SIRT.</li>
      <li>  c. Cooperate and allow the SIRT and DPO to regularly check the operations of the CSD to comply with the former’s obligation in monitoring security breaches and vulnerabilities of computer networks.</li>
      <li>  d. Submit inventory of user access and access level control of employees as to the assigned accounts for the use of the data processing system of the University, e.g., institutional email, NEU Automate, and existing Google forms.</li>
      <li>  e. Secure necessary logs in maintaining the infrastructure of the University.</li>
    </ul>


  
    <p>6.2. COP</p>
    <ul>
      <li>  a. Responsible for conducting data audits within the department. This should involve identifying:
        <ul>
          <li>    i. The categories of personal data processed.</li>
          <li>    ii. The locations where it is stored, e.g., physical filing cabinets, hard disks, and cloud drives.</li>
          <li>    ii. Inflows or sources of personal information, e.g., Google or web forms, physical forms, email, and phone calls.</li>
          <li>    iv. Outflows or third parties with whom the personal data is shared, e.g., mail carriers, advertisers, cloud storage providers, and third-party online platforms.</li>
        </ul>
      </li>
      <li>  b. Responsible for checking that all physical and electronic records are secured, tracked, and/or monitored.</li>
      <li>  c. Records and secures all reports and documents related to suspected security incidents.</li>
      <li>  d. Review and recommend, along with the head of the department, information security measures and the acquisition of necessary equipment to implement such measures to the University President, SIRT, and/or DPO.</li>
      <li>  e. Assist in the implementation of information security measures within the department.</li>
      <li>  f. Submit the Incident Report to the SIRT and DPO in accordance with this policy.</li>
    </ul>
 

  
    <p>6.3. SIRT</p>
    <ul>
      <li>  a. Investigate and assess suspected security incidents in coordination with all concerned units and offices of the University.</li>
      <li>  b. Recommend remedial measures to be performed concerning a suspected security incident.</li>
      <li>  c. Accomplish a Security Incident Report, as prescribed by the Board of Trustees.</li>
      <li>  d. Recommend permanent and additional members of SIRT to the University President.</li>
    </ul>
  

 
    <p>6.4. SIRT Head and DPO</p>
    <ul>
      <li>  a. Serve as the main point of contact for all reports of a suspected security incident.</li>
      <li>  b. Act as custodian of all reports and documents generated or prepared in relation to suspected security incidents. Document all the remedial actions taken.</li>
      <li>  c. Review and revise this Policy in accordance with the Philippine Data Privacy Act of 2012 and other laws, guidelines, and standards applicable to the protection of Data Processing Systems and Personal Data.</li>
      <li>  d. Report to the University President, recommend and implement approved remedial measures to be performed in relation to a suspected security incident.</li>
      <li>  e. Notify the NPC and/or affected data subjects when required by the Data Privacy Act of 2012.</li>
    </ul>
 

  
